package examen2_catalinayushan;

public class Granja {
    int dinero;
    int vaquitas;
    int conejitos;
    int plantitas;

    public int getDinero() {
        return dinero;
    }

    public void setDinero(int dinero) {
        this.dinero = dinero;
    }

    public int getVaquitas() {
        return vaquitas;
    }

    public void setVaquitas(int vaquitas) {
        this.vaquitas = vaquitas;
    }

    public int getConejitos() {
        return conejitos;
    }

    public void setConejitos(int conejitos) {
        this.conejitos = conejitos;
    }

    public int getPlantitas() {
        return plantitas;
    }

    public void setPlantitas(int plantitas) {
        this.plantitas = plantitas;
    }
    
    
    
}
